    private void tablePegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePegawaiMouseClicked
        // TODO add your handling code here:
        btn_simpan.setEnabled(false);
        btn_edit.setEnabled(true);
        btn_hapus.setEnabled(true);
        int i = tablePegawai.getSelectedRow();
        
        if (i == -1) {
            return;
        }
        
        String username = (String) model.getValueAt(i, 0);
        txt_username.setText(username);
        txt_username.setEnabled(false);
        String gender = (String) model.getValueAt(i, 1);
        if(gender.equals("Perempuan")){
            r_female.setSelected(true);
            r_male.setSelected(false);
        } else if (gender.equals("Laki-laki")){
            r_male.setSelected(true);
            r_female.setSelected(false);
        }
        String email = (String) model.getValueAt(i, 2);
        txt_email.setText(email);
        String mobile = (String) model.getValueAt(i, 3);
        txt_mobile.setText(mobile);
        String religion = (String) model.getValueAt(i, 4);
        combo_religion.setSelectedItem(religion);
        String alamat = (String) model.getValueAt(i, 5);
        txt_address.setText(alamat);
    }//GEN-LAST:event_tablePegawaiMouseClicked

