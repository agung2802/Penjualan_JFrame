    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        if(txt_username.getText().equals("") || 
        txt_mobile.getText().equals("")|| txt_email.getText().equals("") || txt_address.getText().equals("")
                || combo_religion.getSelectedItem().toString().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        } else if(r_female.isSelected() && r_male.isSelected()){
            JOptionPane.showMessageDialog(null, "PILIH SALAH SATU JENIS KELAMIN", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        } else if(!r_female.isSelected() && !r_male.isSelected()){
            JOptionPane.showMessageDialog(null, "PILIH SALAH SATU JENIS KELAMINN", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        } else if(!txt_password.getText().equals(txt_password1.getText())){
            JOptionPane.showMessageDialog(null, "RETYPE HARUS SAMA DENGAN PASSWORD", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            String gender = "";
            if(r_female.isSelected() && !r_male.isSelected()){
                gender = "Perempuan";
            } else if(!r_female.isSelected() && r_male.isSelected()){
                gender = "Laki-laki";
            }
            int i = tablePegawai.getSelectedRow();
            if (i == -1) {
                return;
            }
            String user = (String) model.getValueAt(i, 0);
            
            
            try {
                Connection c = Koneksi.getKoneksi();
                String password = "";
                
                try{
                    Statement s = c.createStatement();
                    String getPassword = "SELECT password FROM tbl_login WHERE username='"+ txt_username.getText() +"'";
                    ResultSet r = s.executeQuery(getPassword);
                    while (r.next()) {
                        password = r.getString("password");
                    }
                } catch(Exception e){
                    System.out.println("Error: "+ e);
                }
                
                if (!txt_password.getText().equals("")){
                    password = txt_password.getText();
                }
                String sql = "UPDATE tbl_login SET password='"+ password + "', jenis_kelamin='"+ gender + 
                        "', email='"+ txt_email.getText() + "', no_telp='"+ txt_mobile.getText() +"', agama='"+ combo_religion.getSelectedItem()+
                        "', alamat='"+ txt_address.getText() +"' WHERE username ='" + txt_username.getText() + "'";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Elektronik Berkah", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                loadData();
                txt_username.setText("");
                txt_password.setText("");
                txt_mobile.setText("");
                txt_email.setText("");
                txt_address.setText("");        
                combo_religion.setSelectedIndex(0);
                r_female.setSelected(false);
                r_male.setSelected(false);
                txt_password1.setText("");
                txt_username.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btn_editActionPerformed

