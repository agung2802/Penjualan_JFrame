    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        if(txt_username.getText().equals("") ||txt_password.getText().equals("") || txt_password1.getText().equals("")|| 
        txt_mobile.getText().equals("")|| txt_email.getText().equals("") || txt_address.getText().equals("")
                || combo_religion.getSelectedItem().toString().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        } else if(r_female.isSelected() && r_male.isSelected()){
            JOptionPane.showMessageDialog(null, "PILIH SALAH SATU JENIS KELAMIN", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        } else if(!r_female.isSelected() && !r_male.isSelected()){
            JOptionPane.showMessageDialog(null, "PILIH SALAH SATU JENIS KELAMINN", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        } else if(!txt_password.getText().equals(txt_password1.getText())){
            JOptionPane.showMessageDialog(null, "RETYPE HARUS SAMA DENGAN PASSWORD", "elektronik berkah", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            String username = txt_username.getText();
            String password = txt_password.getText();
            String mobile = txt_mobile.getText();
            String email = txt_email.getText();
            String address = txt_address.getText();             
            String religion = combo_religion.getSelectedItem().toString();
            String gender= ""; 
            
            if(r_female.isSelected() && !r_male.isSelected()){
                gender = "Perempuan";
            } else if(!r_female.isSelected() && r_male.isSelected()){
                gender = "Laki-laki";
            }

            try {
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO tbl_login VALUES (?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, username);
                p.setString(2, password);
                p.setString(3, gender);
                p.setString(4, email);
                p.setString(5, mobile);
                p.setString(6, religion);                 
                p.setString(7, address); 

                p.executeUpdate();
                p.close(); 
                
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "elekronik berkah", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException e) {
                System.out.println("Terjadi Error\n"+ e);
            } finally {
                loadData();
                txt_username.setText("");
                txt_password.setText("");
                txt_mobile.setText("");
                txt_email.setText("");
                txt_address.setText("");        
                combo_religion.setSelectedIndex(0);
                r_female.setSelected(false);
                r_male.setSelected(false);
                txt_password1.setText("");
                txt_username.setEnabled(true);

            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

